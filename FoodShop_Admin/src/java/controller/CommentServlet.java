    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session= request.getSession();
        
        String object=(String)session.getAttribute("iduser");
        int idUser=Integer.parseInt(object); 
        int idProduct=Integer.parseInt(request.getParameter("productid"));
        String content=request.getParameter("content");
        int star=Integer.parseInt(request.getParameter("star"));
        CommentRating a= new CommentRating(idUser,idProduct,content,star);
         try {
            CommentRatingDAO.AddComment(a);
        } catch (Exception ex) {
            Logger.getLogger(CommentServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
        response.sendRedirect("Information?sid="+idProduct);
    }

